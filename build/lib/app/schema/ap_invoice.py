    DocEntry: Optional[int] = Field(None, description="Header document entry reference")
    LineNum: Optional[int] = Field(None, description="Line number")
    ItemCode: str = Field(..., description="SAP item code")
    Dscription: Optional[str] = Field(None, description="Item description")
    Quantity: int = Field(..., gt=0, description="Invoice quantity")
    BaseQty: Optional[float] = Field(None, description="Base quantity from PO or GRPO")
    OpenQty: Optional[float] = Field(None, description="Open quantity")
    OpenInvQty: Optional[float] = Field(None, description="Open invoicing quantity")
    Price: Optional[float] = Field(None, description="Unit price")
    PriceBefDi: Optional[float] = Field(None, description="Price before discount")
    DiscPrcnt: Optional[float] = Field(None, description="Discount percent")
    LineTotal: Optional[float] = Field(None, description="Line total amount")
    Currency: Optional[str] = Field(None, description="Line currency")
    Rate: Optional[float] = Field(None, description="Exchange rate")
    StockPrice: Optional[float] = Field(None, description="Standard cost")
    GrossBuyPr: Optional[float] = Field(None, description="Purchase cost")
    GTotal: Optional[float] = Field(None, description="Line total including tax")
    VatPrcnt: Optional[float] = Field(None, description="Tax percent")
    VatSum: Optional[float] = Field(None, description="Tax amount")
    TaxCode: Optional[str] = Field(None, description="Tax code")
    TaxType: Optional[str] = Field(None, description="GST or VAT type")
    LineVat: Optional[float] = Field(None, description="Line tax")
    BaseType: Optional[int] = Field(None, description="Source document type")
    BaseEntry: Optional[int] = Field(None, description="Source document ID")
    BaseLine: Optional[int] = Field(None, description="Source line number")
    PoTrgEntry: Optional[int] = Field(None, description="Target PO entry")
    TrgetEntry: Optional[int] = Field(None, description="Target document entry")
    WhsCode: Optional[str] = Field(None, description="Warehouse")
    InvntSttus: Optional[str] = Field(None, description="Inventory impact status")
    StockValue: Optional[float] = Field(None, description="Inventory value")
    AcctCode: Optional[str] = Field(None, description="GL account")
    OcrCode: Optional[str] = Field(None, description="Cost center")
    Project: Optional[str] = Field(None, description="Project")
    ShipToCode: Optional[str] = Field(None, description="Delivery location code")
    ShipToDesc: Optional[str] = Field(None, description="Delivery location description")
    TrnsCode: Optional[str] = Field(None, description="Transport code")
    LineStatus: Optional[str] = Field(None, description="Line status")
    FreeTxt: Optional[str] = Field(None, description="Remarks")
    OwnerCode: Optional[int] = Field(None, description="Owner")
    UnitPrice: Optional[float] = Field(None, description="Compatibility unit price field")


